#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main() {
    int i,k;
    for (i = 1; i <= 10; i++) {
        for (k = 1; k <= 10; k++) {
            printf("\n  %d X %d = %d",k,i,k*i);
        }
        printf("\n");
    }
    return 0;
}
  
  
  
